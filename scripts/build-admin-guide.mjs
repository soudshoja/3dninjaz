/**
 * scripts/build-admin-guide.mjs
 *
 * Codegen script — reads every markdown file in src/content/admin-guide/ at
 * BUILD TIME and writes src/lib/admin-guide-generated.ts with all article
 * content baked in as TypeScript constants.
 *
 * This eliminates runtime `fs` access in the admin guide loader, so the
 * content is always available regardless of Next.js standalone bundling or
 * deployment topology.
 *
 * Usage (automatic via package.json prebuild):
 *   node scripts/build-admin-guide.mjs
 */

import { readFileSync, readdirSync, writeFileSync, mkdirSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const CONTENT_DIR = join(ROOT, "src", "content", "admin-guide");
const OUT_FILE = join(ROOT, "src", "lib", "admin-guide-generated.ts");

/**
 * Walk a directory tree and collect all .md file paths relative to base.
 */
function walkMd(dir, base = "") {
  const result = [];
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const rel = base ? `${base}/${entry.name}` : entry.name;
    if (entry.isDirectory()) {
      result.push(...walkMd(join(dir, entry.name), rel));
    } else if (entry.isFile() && entry.name.endsWith(".md")) {
      result.push(rel);
    }
  }
  return result;
}

/**
 * Minimal front-matter parser — handles the exact shapes used in admin-guide
 * markdown files (key: value, key: [a, b, c]).
 */
function parseFrontMatter(raw) {
  if (!raw.startsWith("---")) return { meta: {}, body: raw };
  const end = raw.indexOf("---", 3);
  if (end === -1) return { meta: {}, body: raw };

  const fm = raw.slice(3, end).trim();
  const body = raw.slice(end + 3).trim();
  const meta = {};

  for (const line of fm.split("\n")) {
    const colon = line.indexOf(":");
    if (colon === -1) continue;
    const key = line.slice(0, colon).trim();
    const val = line.slice(colon + 1).trim();

    if (val.startsWith("[")) {
      meta[key] = val
        .slice(1, -1)
        .split(",")
        .map((s) => s.trim().replace(/^['"]|['"]$/g, ""))
        .filter(Boolean);
    } else if (val === "true") {
      meta[key] = true;
    } else if (val === "false") {
      meta[key] = false;
    } else if (val !== "" && !isNaN(Number(val))) {
      meta[key] = Number(val);
    } else {
      meta[key] = val.replace(/^['"]|['"]$/g, "");
    }
  }

  return { meta, body };
}

// Collect and parse all articles
const files = walkMd(CONTENT_DIR);
const articles = [];

for (const relPath of files) {
  const absPath = join(CONTENT_DIR, relPath);
  const raw = readFileSync(absPath, "utf-8");
  const { meta, body } = parseFrontMatter(raw);

  const slug = relPath.replace(/\.md$/, "");
  articles.push({
    slug,
    title: typeof meta.title === "string" ? meta.title : slug,
    category: typeof meta.category === "string" ? meta.category : "General",
    tags: Array.isArray(meta.tags) ? meta.tags : [],
    order: typeof meta.order === "number" ? meta.order : 99,
    content: body,
    href: `/admin/guide/${slug}`,
  });
}

// Sort: category alpha, then order
articles.sort((a, b) => {
  if (a.category !== b.category) return a.category.localeCompare(b.category);
  return a.order - b.order;
});

// Serialise as a TypeScript module
function escapeTemplateLiteral(str) {
  // Escape backticks and ${} so the string is safe inside a template literal
  return str.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
}

const lines = [
  "/**",
  " * AUTO-GENERATED by scripts/build-admin-guide.mjs",
  " * DO NOT EDIT — re-run the script or `npm run build` to regenerate.",
  " *",
  " * All admin guide articles, pre-parsed from src/content/admin-guide/",
  " * at build time. Zero runtime fs access required.",
  " */",
  "",
  'import type { GuideArticle } from "./admin-guide";',
  "",
  "export const GUIDE_ARTICLES: GuideArticle[] = [",
];

for (const a of articles) {
  lines.push("  {");
  lines.push(`    slug: ${JSON.stringify(a.slug)},`);
  lines.push(`    title: ${JSON.stringify(a.title)},`);
  lines.push(`    category: ${JSON.stringify(a.category)},`);
  lines.push(`    tags: ${JSON.stringify(a.tags)},`);
  lines.push(`    order: ${a.order},`);
  lines.push(`    href: ${JSON.stringify(a.href)},`);
  lines.push(`    content: \`${escapeTemplateLiteral(a.content)}\`,`);
  lines.push("  },");
}

lines.push("];");
lines.push("");

const output = lines.join("\n");
writeFileSync(OUT_FILE, output, "utf-8");

console.log(
  `[build-admin-guide] wrote ${articles.length} articles → src/lib/admin-guide-generated.ts`
);
